function sortMapping(mapping) {
	return mapping.sort((a, b) => {
		if (a[0] < b[0]) {
			return -1;
		}
		if (a[0] > b[0]) {
			return 1;
		}
		if (a[1] < b[1]) {
			return -1;
		}
		if (a[1] > b[1]) {
			return 1;
		}
		if (a[2] < b[2]) {
			return -1;
		}
		if (a[2] > b[2]) {
			return 1;
		}
		return 0;
	});
}

function compactMapping(mapping) {
	const board = {};
	sortMapping(mapping).forEach(m => {
		board[m[0]] = board[m[0]] || {};
		board[m[0]][m[2]] = board[m[0]][m[2]] || [];
		board[m[0]][m[2]].push(m[1]);
	});
	const result = [];
	for (const z of Object.keys(board)) {
		const rows = [];
		for (const y of Object.keys(board[z])) {
			const a = board[z][y];
			const entries = [];
			let entry = {start: -1, current: -1, count: 0};
			a.forEach(x => {
				if (x !== entry.current) {
					entry = {start: x, current: x + 2, count: 1};
					entries.push(entry);
				} else {
					entry.current += 2;
					entry.count++;
				}
			});
			const cells = entries.map(e => {
				if (e.count === 1) {
					return e.start;
				}
				return [e.start, e.count];
			});
			if (cells.length === 1 && !Array.isArray(cells[0])) {
				rows.push([Number(y), cells[0]]);
			} else {
				rows.push([Number(y), cells]);
			}
		}
		result.push([Number(z), rows]);
	}
	return result;
}

function expandMapping(map) {
  const result = [];
  map.forEach(matrix => {
    const z = matrix[0];
    const rows = matrix[1];
    rows.forEach(row => {
      const y = row[0];
      const cells = row[1];
      if (!Array.isArray(cells)) {
        result.push([z, cells, y]);
      } else {
        cells.forEach(cell => {
          if (Array.isArray(cell)) {
            let x = cell[0];
            const count = cell[1];
            for (let i = 0; i < count; i++) {
              result.push([z, x, y]);
              x += 2;
            }
          } else {
            result.push([z, cell, y]);
          }
        });
      }
    });
  });
  return result;
}


